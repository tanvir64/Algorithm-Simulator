package client;

public class edge2Info {
    private double edge2X,edg2Y;

    public edge2Info(double edge2X, double edg2Y) {
        this.edge2X = edge2X;
        this.edg2Y = edg2Y;
    }

    public double getEdge2X() {
        return edge2X;
    }

    public double getEdg2Y() {
        return edg2Y;
    }
}
